    private void btn_tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tambahActionPerformed
        soundClick();
        String notelp1 = txt_notelp.getText();       
        char[] chars = notelp1.toCharArray();
               
        if (txt_idkaryawan.getText().equals("")|| txt_namakaryawan.getText().equals("") || txt_notelp.getText().equals("")){
            JOptionPane.showMessageDialog(null,"lengkapi data");                          
            
        } else if(chars[0]!='0'){
                JOptionPane.showMessageDialog(null, "Format nomor dimulai dengan 08");
                txt_notelp.setText("");
        } else if(chars.length<12){
                JOptionPane.showMessageDialog(null, "Format nomor minimal 12 angka");
                
        }else if(chars.length>13){
                JOptionPane.showMessageDialog(null, "Format nomor maksimal 13 angka");
        }
        
        else {
            String idkaryawan = txt_idkaryawan.getText();
            String namakaryawan = txt_namakaryawan.getText();
            String notelp = txt_notelp.getText();
            String alamat = txt_alamat.getText();
                try {
                    Connection c = koneksi.getKoneksi();
                    String sql = "INSERT INTO member VALUES (?,?,?,?)";
                    
                    PreparedStatement p = c.prepareStatement(sql);
                    p.setString(1, idkaryawan);
                    p.setString(2, namakaryawan);
                    p.setString(3, notelp);
                    p.setString(4, alamat);
                    
                    p.executeUpdate();
                    p.close();
                } catch (SQLException e){
                    JOptionPane.showMessageDialog(null, "terjadi kesalahan");
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(frameDataMasterMember.class.getName()).log(Level.SEVERE, null, ex);
                }
               kode();
                
               kosong();
                
                JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
                
                }
        idkaryawan();
    }//GEN-LAST:event_btn_tambahActionPerformed

